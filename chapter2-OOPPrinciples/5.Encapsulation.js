/**
 * Objects represent the typical expression of encapsulation, that is, the ability to concontrate in one entity
 * both data (properties) and functions (methods), hiding the internal details.
 * 
 * In other words, the encapsulation principle allows an object to expose just what is needed to use it, hiding
 * the complexity of its implementation. This is a very powerful principle, often found in the real world that 
 * allows us to use an object without knowing how it internally works. Consider for example how we drive cars. 
 * We just need to know how to speed up, brake, and change direction. We do not need to know how the car works in
 * detail, how it motor burns fuel or transmits movement to the wheels.
 */